package search_result;

import java.io.*;
import java.util.*;
import syntree.*;

/**
 * a list of all the SentenceResults generated by one sentence,
 * in the order they were generated.
 */

public class History {

    private Vector all_results;
    private BitSet same_bound;
    private int same_bound_index;
    private SubResult nullster = new SubResult("NULL");

    public History () {
	this.all_results = new Vector(); } 

    public void addSentenceResult (SentenceResult sr) {
	(this.all_results).addElement(sr); }

    public SentenceResult sentenceResultAt(int i) {
	return ((SentenceResult)all_results.elementAt(i)); }

    public boolean isEmpty() {
	return (all_results.isEmpty()); }

    public int size() {
	return ((this.all_results).size()); }
    
    public void removeAllElements() {
	all_results.removeAllElements(); }

    public SentenceResult sentResPerBoundary (Node in_bound, int index) {
	SentenceResult oldsr, newsr = new SentenceResult();
	SubResult oldsubb;
	int j;

	oldsr = this.sentenceResultAt(index);
	for (j = 0; j < oldsr.size(); j++) {
	    oldsubb = oldsr.subResultAt(j);
	    if ((oldsubb.getBoundary()).equals(in_bound)) {
		newsr.addSubResult(oldsubb); } }
	return newsr; }
	
    public void PrintToSystemErr () {
	int i;
	SentenceResult sr;
	for (i = 0;i < all_results.size(); i++) {
	    sr = (SentenceResult)all_results.elementAt(i);
	    System.err.print(i + ".)  ");
	    sr.PrintToSystemErr(); }
	return; }
}






